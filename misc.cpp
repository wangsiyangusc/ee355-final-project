string codeName(string str1, string str2) {
    string result = "";
    
    // Concatenate str1 and str2 without spaces
    for (char c : str1) {
        if (c != ' ') {
            result += tolower(c);
        }
    }
    for (char c : str2) {
        if (c != ' ') {
            result += tolower(c);
        }
    }
    
    return result;
}